/**
 * 
 * @param {Boolean} noAjax
 * @returns {undefined}
 */
function processResponse(noAjax) {
    //check if we have form in fancybox
    var form = $(document).find('.fancybox-inner form');
    if (form && !noAjax) {
        form.submit(function (e) {
            var $form = $(this);
            $form.find('button[type=submit]').prop('disabled', true);
            $.ajax({
                type: 'POST',
                cache: false,
                url: form.attr('action'),
                data: form.serialize(),
                async: true,
                dataType: 'json',
                success: function (response) {
                    processResponse(response);
                }

            });
            return false;
        });
    }
}

