// lightbox init
function initLightbox() {
    jQuery('a.lightbox, a[rel*="lightbox"]').fancybox({
        helpers: {
            overlay: {
                css: {
                    background: 'rgba(0, 0, 0, 0.65)'
                }
            }
        },
        afterLoad: function (current, previous) {
            // handle custom close button in inline modal
            if (current.href.indexOf('#') === 0) {
                jQuery(current.href).find('a.close').off('click.fb').on('click.fb', function (e) {
                    e.preventDefault();
                    jQuery.fancybox.close();
                });
            }
        },
    });

    jQuery('a.lightbox-ajax, a[rel*="lightbox-ajax"]').click(function (event) {
        event.preventDefault();
        var $this = $(this), link = $this.attr('href'), method = ($this.attr('method') || 'GET'),
                width = ($this.data('lightbox-width') || 800), height = ($this.data('lightbox-height') || 400)
        ;
        jQuery.fancybox.open({
            href: link,
            type: 'ajax',
            hideOnContentClick: false,
            showCloseButton: false,
            closeBtn: false,
            autoSize: false,
            closeClick: false,
            width: width,
            height: height,
            ajax: {
                type: method
            },
            helpers: {
                overlay: {
                    css: {
                        background: 'rgba(0, 0, 0, 0.65)',
                        closeClick: false
                    }
                }
            },
            afterLoad: function (current, previous) {
                var noAjax = $this.data('noajax');
                processResponse(noAjax);

                // handle custom close button in inline modal
                if (current.href.indexOf('#') === 0) {
                    jQuery(current.href).find('a.close').off('click.fb').on('click.fb', function (e) {
                        e.preventDefault();
                        jQuery.fancybox.close();
                    });
                }
            },
        });
        return false;
    });
}

